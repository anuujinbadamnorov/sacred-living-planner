// Dinner rotation helpers
export const DINNER_RECIPES = RECIPES.filter((r) => r.category === 'dinner' || r.category === 'soup')
export const BREAKFAST_RECIPES = RECIPES.filter((r) => r.category === 'breakfast')
export const LIGHT_RECIPES = RECIPES.filter((r) => r.category === 'lunch' || r.category === 'snack')

// Recipes that are warming/gentle — good for menstrual & late-luteal days
export const WARMING_TAGS = ['warming', 'brothy', 'gentle', 'medicinal', 'comfort']

export function getDinnersForWeek(weekOfYear: number): Recipe[] {
  const pool = [...DINNER_RECIPES]
  const out: Recipe[] = []
  let seed = weekOfYear * 2654435761
  for (let i = 0; i < 7 && pool.length > 0; i++) {
    seed = (seed * 1103515245 + 12345) % 2147483648
    const idx = seed % pool.length
    out.push(pool.splice(idx, 1)[0])
  }
  return out
}
